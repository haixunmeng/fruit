package fruit.market.data;

public class Good {
	
	private String good_id;
	
	private String good_name;
	
	private String good_photo_url;
	
	private String good_desc;
	
	private String creator;
	
	private String create_time;

	public String getGood_id() {
		return good_id;
	}

	public void setGood_id(String good_id) {
		this.good_id = good_id;
	}

	public String getGood_name() {
		return good_name;
	}

	public void setGood_name(String good_name) {
		this.good_name = good_name;
	}

	public String getGood_photo_url() {
		return good_photo_url;
	}

	public void setGood_photo_url(String good_photo_url) {
		this.good_photo_url = good_photo_url;
	}

	public String getGood_desc() {
		return good_desc;
	}

	public void setGood_desc(String good_desc) {
		this.good_desc = good_desc;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
}
